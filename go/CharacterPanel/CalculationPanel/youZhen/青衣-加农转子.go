func 悠真_加农转子1_朋克青衣01丽娜11() *Initializations {
	name := "悠真_加农转子1_朋克青衣01丽娜11"
	init := &Initializations{
		Name:       name,
		NumberFour: common.ExplosiveInjury,
	}
	// 初始化基础数值
	init.InitializationBase朋克(Role.YouZhen(), arms.JiaNongZhuanZi(true, 1))
	// 初始化角色增益
	init.InitializationRole([]*Role.BuffCharacter{Role.QingYi01(), Role.LiNa11()})

	init.Initializations = []*Initialization{
		{
			Magnifications: MagnificationBase加农转子(),
			Gain: &Gain{
				Vulnerable: 50,
			},
			Name:         name + "-失衡",
			Output:       &Output{},
			CurrentPanel: &CurrentPanel{},
		},
	}
	return init
}

